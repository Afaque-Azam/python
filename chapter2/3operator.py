# bitwise operator

a, b = 6, 3   # (6 = 110, 3 = 011 in binary)

print(a & b)   # 2 (AND → 110 & 011 = 010)
print(a | b)   # 7 (OR  → 110 | 011 = 111)
print(a ^ b)   # 5 (XOR → 110 ^ 011 = 101)
print(~a)      # -7 (NOT → inverts bits)
print(a << 1)  # 12 (Left shift → 1100)
print(a >> 1)  # 3  (Right shift → 011)

